    // c) Add to Basket
    /*
    Reads quantity from txtQuantity.
    Checks if a product is selected and if enough stock is available.
    Creates an OrderLine and adds it to the current order.
    */
    private void btnAddToBasketActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddToBasketActionPerformed
        
        // error detection
        // select an product from 2nd list
        // unselected is -1
        
        // Check if a category is selected
        if (lstCategories.getSelectedIndex() == -1) 
        {
            lblConfirmation.setText("Please select a category first.");
            return;
        }

        // Check if is a product is selected
        if(lstProductsByCategory.getSelectedIndex() == -1)// no selected
        {
            lblConfirmation.setText("Please select a product from the list.");
            return;
        }
        
        // TODO DONE invalid quantity input (negative, 0, non integer numbers, non numbers) handling error
        
        // create and initialise variable quantity from the textbox
        String textquantity = txtQuantity.getText();
        
     
        // Check if quantity field is empty
        // isEmpty() can only be called on a String object, quantity is int
        if (textquantity.isEmpty())// or textquantity.isEmpty()
        {
            lblConfirmation.setText("Please enter a quantity");
            return; //stop further execution
        }        
        
        int q;
        
        try{
            q = parseInt(textquantity);
            if(q<=0)
            {
                lblConfirmation.setText("Quantity must be a positive number.");
                return;
            }
        }catch (NumberFormatException e)
            {
                lblConfirmation.setText("Invalid quantity. Please enter a whole number.");
                return;
            }
               
        
        
        // get the product which is selected
        // convert into an Product
            
        // getSelectedValue() returns a String, it cannot convert directly into String
        //change to: top level Object that includes a String
        Object selectedProductObject = (Object)lstProductsByCategory.getSelectedValue();                        
        Product selectedProduct =(Product)selectedProductObject;
            
        int stock = selectedProduct.getStockLevel();
            
        // Check stock availability, now that I got the Product 
            if (stock>=q) //  TODO check
            {
                // create OrderLine with that product
                OrderLine ol = new OrderLine(selectedProduct,q);// ctrl + space bar to get the parameters
                
                // create an getter for orderlineId
                // DO NOT COPY THIS LINE
                //currentOrder.getOrderLines().put (ol.getOrderLineId() ,ol);
                
                currentOrder.addOrderLine(ol);
                lblConfirmation.setText("Product Added to Basket");
            }
            else
            {
                lblConfirmation.setText("Only " + stock + " units available.Please, reduce quantity.");
            }
    }//GEN-LAST:event_btnAddToBasketActionPerformed

