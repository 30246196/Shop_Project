    // a) Category Selection
    /*
    When a category is selected, it filters products by category and updates the second list (lstProductsByCategory).
    */
    //method triggered by ListSelectionEvent whenever the user selects a different category in the lstCategories list
    private void lstCategoriesValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_lstCategoriesValueChanged
        // 
        String selectedCategory = lstCategories.getSelectedValue();//retrieve the selected category
        
        // creates an empty model to hold products filtered by category
        DefaultListModel categoryModel = new DefaultListModel();// create a new DefaultListModel to hold products of that category --fix imports
        
        // see User or Customer class file
        // loops through allProducts and 
        for(Product p: allProducts)
        {
            //checks if each product belongs to the selected category ("Solar Panel" or "Heat Pump" or "ReplacementPart")
                                            //package.class name
            if (p.getClass().getName().equals("models." + selectedCategory))//
            {
                categoryModel.addElement(p);//add the product p to the model
            }
        }
        
        lstProductsByCategory.setModel(categoryModel);// updates the second list (lstProductsByCategory)
        
    }//GEN-LAST:event_lstCategoriesValueChanged

